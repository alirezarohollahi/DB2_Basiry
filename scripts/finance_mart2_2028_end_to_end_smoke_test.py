def ensure_conformed_dim_table(conn: pyodbc.Connection, table: str, key_col: str, id_col: str, name_col: str) -> None:
    full = f"{DW_DB}.dw.{table}"
    if table_exists(conn, full):
        return
    if not ENSURE_MINIMAL_CONFORMED_DIMS:
        raise RuntimeError(f"Missing required conformed dimension: {full}")

    print(f"Creating minimal test-only {full} because it does not exist...")
    exec_sql(
        conn,
        f"""
        USE {DW_DB};
        IF NOT EXISTS (SELECT 1 FROM sys.schemas WHERE name = N'dw') EXEC(N'CREATE SCHEMA dw');
        CREATE TABLE dw.{table} (
            {key_col} INT IDENTITY(1,1) NOT NULL,
            {id_col} INT NULL,
            {name_col} NVARCHAR(200) NULL,
            source_system NVARCHAR(100) NULL,
            created_at DATETIME2(0) NULL,
            updated_at DATETIME2(0) NULL
        );
        """,
    )
    conn.commit()


def insert_conformed_dim_row(conn: pyodbc.Connection, table: str, id_col: str, id_value: int, values: dict[str, Any]) -> None:
    full = f"{DW_DB}.dw.{table}"
    if not table_exists(conn, full):
        raise RuntimeError(f"Missing table: {full}")
    if not col_exists(conn, full, id_col):
        raise RuntimeError(f"{full} must have column {id_col}")

    exists = scalar(conn, f"SELECT COUNT(1) FROM {full} WHERE {id_col} = ?", [id_value])
    if exists:
        return

    available_cols = [r[0] for r in fetch_all(
        conn,
        """
        SELECT c.name
        FROM ?.sys.columns c
        """.replace("?", DW_DB) + f" WHERE c.object_id = OBJECT_ID('{DW_DB}.dw.{table}')",
    )]

    insert_cols: list[str] = []
    insert_vals: list[Any] = []
    all_values = {id_col: id_value, **values}
    for col, val in all_values.items():
        if col in available_cols:
            insert_cols.append(col)
            insert_vals.append(val)

    if id_col not in insert_cols:
        raise RuntimeError(f"Cannot insert {full}; missing {id_col}")

    sql = f"INSERT INTO {full} ({', '.join(insert_cols)}) VALUES ({', '.join('?' for _ in insert_cols)});"
    exec_sql(conn, sql, insert_vals)


