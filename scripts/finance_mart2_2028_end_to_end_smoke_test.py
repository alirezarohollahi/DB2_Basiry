def dec(value: Any) -> Decimal:
    if value is None:
        return Decimal("0.00")
    return Decimal(str(value)).quantize(Decimal("0.01"))


def get_center_key(conn: pyodbc.Connection, center_id: int) -> int:
    val = scalar(conn, f"SELECT TOP (1) center_key FROM {DW_DB}.dw.dim_center WHERE center_id = ?", [center_id])
    return int(val) if val is not None else -1


def snapshot_row(conn: pyodbc.Connection, month_key: int, center_key: int) -> pyodbc.Row | None:
    return fetch_one(
        conn,
        f"""
        SELECT month_key, center_key, total_donation_amount, total_expense_amount, total_payment_amount,
               net_balance, donation_count, expense_count, payment_count, allocation_count
        FROM {DW_DB}.dw.fact_monthly_financial_snapshot
        WHERE month_key = ? AND center_key = ?;
        """,
        [month_key, center_key],
    )


def verify_snapshot(
    conn: pyodbc.Connection,
    results: list[tuple[str, bool, str]],
    label: str,
    month_key: int,
    center_key: int,
    expected: dict[str, Any],
) -> None:
    row = snapshot_row(conn, month_key, center_key)
    if row is None:
        assert_or_record(results, label, False, f"missing snapshot row month_key={month_key}, center_key={center_key}")
        return

    checks = {
        "total_donation_amount": dec(row.total_donation_amount) == dec(expected["total_donation_amount"]),
        "total_expense_amount": dec(row.total_expense_amount) == dec(expected["total_expense_amount"]),
        "total_payment_amount": dec(row.total_payment_amount) == dec(expected["total_payment_amount"]),
        "net_balance": dec(row.net_balance) == dec(expected["net_balance"]),
        "donation_count": int(row.donation_count or 0) == int(expected["donation_count"]),
        "expense_count": int(row.expense_count or 0) == int(expected["expense_count"]),
        "payment_count": int(row.payment_count or 0) == int(expected["payment_count"]),
        "allocation_count": int(row.allocation_count or 0) == int(expected["allocation_count"]),
    }
    ok = all(checks.values())
    detail = "actual=(don={0}, exp={1}, pay={2}, net={3}, dc={4}, ec={5}, pc={6}, ac={7})".format(
        row.total_donation_amount,
        row.total_expense_amount,
        row.total_payment_amount,
        row.net_balance,
        row.donation_count,
        row.expense_count,
        row.payment_count,
        row.allocation_count,
    )
    if not ok:
        failed = [k for k, v in checks.items() if not v]
        detail += f" failed={failed} expected={expected}"
    assert_or_record(results, label, ok, detail)


