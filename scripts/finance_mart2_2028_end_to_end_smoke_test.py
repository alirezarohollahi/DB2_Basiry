def print_summary(all_results: list[tuple[str, bool, str]]) -> None:
    total = len(all_results)
    failed = [r for r in all_results if not r[1]]
    print("\n" + "=" * 80)
    print(f"SMOKE TEST SUMMARY: {total - len(failed)}/{total} passed")
    if failed:
        print("FAILED CHECKS:")
        for name, _, detail in failed:
            print(f"  - {name}: {detail}")
        sys.exit(1)
    print("All checks passed.")


